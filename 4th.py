string = "hello world"

# capitalize() method
print(string.capitalize())

# upper() method
print(string.upper())

# lower() method
print(string.lower())

# count() method
print(string.count('l'))

# replace() method
print(string.replace('world', 'python'))

# split() method
print(string.split(' '))

# join() method
words = ['hello', 'world']
print(' '.join(words))
